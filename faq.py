# Қазақ тіліне тән әріптер
_KAZAKH_CHARS = set("әіғқңөұүһӘІҒҚҢӨҰҮҺ")


def detect_language(text: str) -> str:
    """Мәтін тілін анықтайды: 'kz' немесе 'ru'."""
    if any(ch in _KAZAKH_CHARS for ch in text):
        return "kz"
    return "ru"


def find_faq(text: str) -> str | None:
    """Мәтіннен FAQ жауабын іздейді, тілді анықтап жауап береді."""
    normalized = text.lower().strip()
    lang = detect_language(text)
    answer_key = f"answer_{lang}"
    for topic_data in FAQ_DATA.values():
        for keyword in topic_data["keywords"]:
            if keyword in normalized:
                return topic_data[answer_key]
    return None
